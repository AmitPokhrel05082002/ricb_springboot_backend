package bt.ricb.ricb_api.models;

public class VehicleModel {
	 private String mfgCode;
	    private String cmName;
	    private String modelCode;
	    private String modelName;
	    private String modelVariant;
	    private String cc;
	    private String fuel;
	    private String weight;
	    private String passengerCapacity;
		public String getMfgCode() {
			return mfgCode;
		}
		public void setMfgCode(String mfgCode) {
			this.mfgCode = mfgCode;
		}
		public String getCmName() {
			return cmName;
		}
		public void setCmName(String cmName) {
			this.cmName = cmName;
		}
		public String getModelCode() {
			return modelCode;
		}
		public void setModelCode(String modelCode) {
			this.modelCode = modelCode;
		}
		public String getModelName() {
			return modelName;
		}
		public void setModelName(String modelName) {
			this.modelName = modelName;
		}
		public String getModelVariant() {
			return modelVariant;
		}
		public void setModelVariant(String modelVariant) {
			this.modelVariant = modelVariant;
		}
		public String getCc() {
			return cc;
		}
		public void setCc(String cc) {
			this.cc = cc;
		}
		public String getFuel() {
			return fuel;
		}
		public void setFuel(String fuel) {
			this.fuel = fuel;
		}
		public String getWeight() {
			return weight;
		}
		public void setWeight(String weight) {
			this.weight = weight;
		}
		public String getPassengerCapacity() {
			return passengerCapacity;
		}
		public void setPassengerCapacity(String passengerCapacity) {
			this.passengerCapacity = passengerCapacity;
		}
	    
	    

}
